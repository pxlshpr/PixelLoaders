import SwiftUI

/// A configurable pixel-grid loading animation.
///
/// Renders an animated grid of rounded squares whose opacities cycle through
/// pre-computed frame data, creating a pulsing/sweeping effect.
///
/// ```swift
/// // Default "thinking" style
/// PixelGridLoader()
///
/// // Built-in "debugging" style
/// PixelGridLoader(style: .debugging)
///
/// // Fully custom
/// PixelGridLoader(frames: myFrames, color: .green, pixelSize: 12)
/// ```
public struct PixelGridLoader: View {

    private let frames: [[Double]]
    private let color: Color
    private let gridSize: Int
    private let pixelSize: CGFloat
    private let gap: CGFloat
    private let cornerRadius: CGFloat
    private let cycleDuration: TimeInterval

    @State private var startDate = Date()

    /// Creates a pixel grid loader with explicit parameters.
    ///
    /// - Parameters:
    ///   - frames: 2D array of opacity values. Each inner array has `gridSize * gridSize`
    ///     `Double` values (0.0–1.0). The outer array is one entry per animation frame.
    ///   - color: The fill color for every pixel.
    ///   - gridSize: Number of rows and columns in the grid.
    ///   - pixelSize: Width and height of each pixel square in points.
    ///   - gap: Spacing between pixels in points.
    ///   - cornerRadius: Corner radius of each pixel square.
    ///   - cycleDuration: Seconds for one complete loop through all frames.
    public init(
        frames: [[Double]] = Self.thinkingFrames,
        color: Color = .purple,
        gridSize: Int = 5,
        pixelSize: CGFloat = 8,
        gap: CGFloat = 3,
        cornerRadius: CGFloat = 2,
        cycleDuration: TimeInterval = 1.0
    ) {
        self.frames = frames
        self.color = color
        self.gridSize = gridSize
        self.pixelSize = pixelSize
        self.gap = gap
        self.cornerRadius = cornerRadius
        self.cycleDuration = cycleDuration
    }

    /// Creates a pixel grid loader from a built-in style.
    ///
    /// - Parameters:
    ///   - style: A ``PixelGridLoaderStyle`` that provides frames and a default color.
    ///   - pixelSize: Width and height of each pixel square in points.
    ///   - gap: Spacing between pixels in points.
    ///   - cornerRadius: Corner radius of each pixel square.
    ///   - cycleDuration: Seconds for one complete loop through all frames.
    public init(
        style: PixelGridLoaderStyle,
        pixelSize: CGFloat = 8,
        gap: CGFloat = 3,
        cornerRadius: CGFloat = 2,
        cycleDuration: TimeInterval = 1.0
    ) {
        self.frames = style.frames
        self.color = style.defaultColor
        self.gridSize = 5
        self.pixelSize = pixelSize
        self.gap = gap
        self.cornerRadius = cornerRadius
        self.cycleDuration = cycleDuration
    }

    public var body: some View {
        TimelineView(.animation) { timeline in
            let elapsed = timeline.date.timeIntervalSince(startDate)
            let progress = elapsed.truncatingRemainder(dividingBy: cycleDuration) / cycleDuration
            let frameIndex = min(Int(progress * Double(frames.count)), frames.count - 1)
            let opacities = frames[frameIndex]

            Grid(horizontalSpacing: gap, verticalSpacing: gap) {
                ForEach(0..<gridSize, id: \.self) { row in
                    GridRow {
                        ForEach(0..<gridSize, id: \.self) { col in
                            let index = row * gridSize + col
                            let o = index < opacities.count ? opacities[index] : 0
                            RoundedRectangle(cornerRadius: cornerRadius)
                                .fill(color)
                                .frame(width: pixelSize, height: pixelSize)
                                .opacity(o)
                                .shadow(color: color.opacity(o * 0.5), radius: 3 * o)
                        }
                    }
                }
            }
        }
    }
}

// MARK: - Built-in Styles

/// Predefined animation styles for ``PixelGridLoader``.
public enum PixelGridLoaderStyle: String, CaseIterable {
    /// A pulsing pattern — good for "thinking" or "processing" states.
    case thinking
    /// A diagonal sweep pattern — good for "debugging" or "scanning" states.
    case debugging

    /// The default color for this style.
    public var defaultColor: Color {
        switch self {
        case .thinking:  return .purple
        case .debugging: return .blue
        }
    }

    /// The frame data for this style (60 frames × 25 cells).
    public var frames: [[Double]] {
        switch self {
        case .thinking:  return PixelGridLoader.thinkingFrames
        case .debugging: return PixelGridLoader.debuggingFrames
        }
    }
}

